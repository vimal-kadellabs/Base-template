export const getAvailableColorSchemes = () => Object.values(colorSchemes);

export const getColorSchemeById = (schemeId) => 
  colorSchemes[schemeId] || colorSchemes[COLOR_SCHEMES.DEFAULT];

export const isValidColorScheme = (schemeId) => 
  Object.values(COLOR_SCHEMES).includes(schemeId);

/**
 * Get color values for a specific scheme and mode
 * @param {string} schemeId - Color scheme ID
 * @param {string} mode - 'light' or 'dark'
 * @returns {object} Color values for the scheme and mode
 */
export const getColorValues = (schemeId, mode) => {
  const scheme = getColorSchemeById(schemeId);
  return scheme[mode] || scheme.light;
};
